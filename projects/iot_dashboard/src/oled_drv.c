/* GPIO 编号（野火 STM32MP157 Pro，GPIOF base=80，PB base=16） */
#define GPIO_CLK  "90"   /* PF10 -> SPI_CLK */
#define GPIO_SDA  "88"   /* PF8  -> SPI_IO0 */
#define GPIO_DC   "89"   /* PF9  -> SPI_IO1 */
#define GPIO_RES  "87"   /* PF7  -> SPI_IO2 */
#define GPIO_CS   "22"   /* PB6  -> SPI_NCS */

/* 显存缓冲（128 列 x 8 页 = 1024 字节） */
static unsigned char g_vram[128 * 8];

/* ═══════════════ GPIO 底层 ═══════════════ */

static int gpio_export(const char *pin) {
    int fd = open("/sys/class/gpio/export", O_WRONLY);
    if (fd < 0) return -1;
    write(fd, pin, strlen(pin));
    close(fd);
    usleep(50000);
    return 0;
}

static int gpio_direction(const char *pin, const char *dir) {
    char path[64];
    snprintf(path, sizeof(path), "/sys/class/gpio/gpio%s/direction", pin);
    int fd = open(path, O_WRONLY);
    if (fd < 0) {
        fprintf(stderr, "[oled] gpio%s direction failed: %s\n", pin, strerror(errno));
        return -1;
    }
    write(fd, dir, strlen(dir));
    close(fd);
    return 0;
}

static int gpio_write(const char *pin, int val) {
    char path[64];
    snprintf(path, sizeof(path), "/sys/class/gpio/gpio%s/value", pin);
    int fd = open(path, O_WRONLY);
    if (fd < 0) return -1;
    write(fd, val ? "1" : "0", 1);
    close(fd);
    return 0;
}

/* ═══════════════ SPI bit-bang ═══════════════ */

static void spi_send_byte(unsigned char data) {
    for (int i = 7; i >= 0; i--) {
        gpio_write(GPIO_CLK, 0);
        gpio_write(GPIO_SDA, (data >> i) & 0x01);
        gpio_write(GPIO_CLK, 1);
    }
    gpio_write(GPIO_CLK, 0);
}

static void oled_write_cmd(unsigned char cmd) {
    gpio_write(GPIO_DC, 0);
    gpio_write(GPIO_CS, 0);
    spi_send_byte(cmd);
    gpio_write(GPIO_CS, 1);
}

static void oled_write_data(unsigned char data) {
    gpio_write(GPIO_DC, 1);
    gpio_write(GPIO_CS, 0);
    spi_send_byte(data);
    gpio_write(GPIO_CS, 1);
}

