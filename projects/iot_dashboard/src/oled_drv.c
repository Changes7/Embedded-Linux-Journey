/* ═══════════════ 公共接口实现 ═══════════════ */

int oled_init(void) {
    const char *pins[] = {GPIO_CLK, GPIO_SDA, GPIO_DC, GPIO_RES, GPIO_CS};
    for (int i = 0; i < 5; i++) {
        gpio_export(pins[i]);
        if (gpio_direction(pins[i], "out") < 0)
            return -1;
    }

    gpio_write(GPIO_CS, 1);
    gpio_write(GPIO_DC, 0);
    gpio_write(GPIO_RES, 1); usleep(100000);
    gpio_write(GPIO_RES, 0); usleep(200000);
    gpio_write(GPIO_RES, 1); usleep(200000);

    oled_write_cmd(0xAE);
    oled_write_cmd(0xD5); oled_write_cmd(0x80);
    oled_write_cmd(0xA8); oled_write_cmd(0x3F);
    oled_write_cmd(0xD3); oled_write_cmd(0x00);
    oled_write_cmd(0x40);
    oled_write_cmd(0x8D); oled_write_cmd(0x14);
    oled_write_cmd(0x20); oled_write_cmd(0x00);
    oled_write_cmd(0xA1);
    oled_write_cmd(0xC8);
    oled_write_cmd(0xDA); oled_write_cmd(0x12);
    oled_write_cmd(0x81); oled_write_cmd(0xFF);
    oled_write_cmd(0xD9); oled_write_cmd(0xF1);
    oled_write_cmd(0xDB); oled_write_cmd(0x40);
    oled_write_cmd(0xA4);
    oled_write_cmd(0xA6);
    oled_write_cmd(0xAF);

    memset(g_vram, 0, sizeof(g_vram));
    oled_refresh();
    return 0;
}

void oled_clear(void) {
    memset(g_vram, 0, sizeof(g_vram));
    oled_refresh();
}

void oled_fill(unsigned char data) {
    memset(g_vram, data, sizeof(g_vram));
    oled_refresh();
}

void oled_refresh(void) {
    oled_write_cmd(0x21); oled_write_cmd(0x00); oled_write_cmd(0x7F);
    oled_write_cmd(0x22); oled_write_cmd(0x00); oled_write_cmd(0x07);
    for (int i = 0; i < 1024; i++)
        oled_write_data(g_vram[i]);
}

void oled_show_string(int x, int page, const char *str) {
    while (*str && x <= 122) {
        unsigned char c = (unsigned char)*str;
        if (c < 0x20 || c > 0x7E) c = '?';
        int idx = c - 0x20;
        for (int col = 0; col < 6; col++)
            g_vram[page * 128 + x + col] = font6x8[idx][col];
        x += 6;
        str++;
    }
}

void oled_show_num(int x, int page, int num) {
    char buf[16];
    snprintf(buf, sizeof(buf), "%d", num);
    oled_show_string(x, page, buf);
}
